package login;

public class TK {
	String user;
	String mk;
	public TK() {
		super();
	}
	public TK(String user, String mk) {
		super();
		this.user = user;
		this.mk = mk;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getMk() {
		return mk;
	}
	public void setMk(String mk) {
		this.mk = mk;
	}

}
